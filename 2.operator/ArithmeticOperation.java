
class ArithmeticOperation{
	public static void main(String[] args) {
		int i = 10;
		System.out.println("Content of i is: "+i);

		int j = 5;
		System.out.println("Content of j is: "+j);

		int  a = i+j;
		System.out.println("Addition of "+i+" and "+j+" is: "+a);

		int b = i-j;
		System.out.println("Substraction of "+i+" and "+j+" is: "+b);

		int c = i*j;
		System.out.println("Multiplication of "+i+" and "+j+" is: "+c);

		int d = i/j;
		System.out.println("Division of "+i+" and "+j+" is: "+d);
		
}
}